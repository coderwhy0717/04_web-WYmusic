function reducer(state = defaultState, action) {
  switch (action.type) {
    case actionType.CHANGE_PLAY_LIST:
      return state.set('playList', action.playList)
    case actionType.CHANGE_CURRENT_SONG_INDEX:
      return state.set('currentSongIndex', action.currentSongIndex)
    case actionType.CHANGE_SEQUENCE:
      return state.set('sequence', action.sequence)
    case actionType.CHANGE_CURRENT_SONG:
      return state.set('currentSong', action.songs)
    case actionType.CHANGE_PLAY_LYRICS:
      return state.set('playLyrics', action.lyrics)
    case actionType.CHANGE_PAGES_LYRICS:
      return state.set('pagesLyrics', action.lyrics)
    case actionType.CHANGE_LYRICS_INDEX:
      return state.set('lyricsIndex', action.index)
    case actionType.CHANGE_ONPLAYING:
      return state.set('onplaying', action.playing)
    case actionType.CHANGE_MASK_COVER:
      return state.set('maskCover', action.show)
    case actionType.CHANGE_OPEN:
      return state.set('open', action.open)
    default:
      return state
  }
}

export default reducer
